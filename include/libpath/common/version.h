


#define LIBPATH_VER_MAJOR       0
#define LIBPATH_VER_MINOR       1
#define LIBPATH_VER_REVISION    4
#define LIBPATH_VER_ALPHABETA   0

#define LIBPATH_VER             (\
                                (   (LIBPATH_VER_MAJOR)     << 24   )   |\
                                (   (LIBPATH_VER_MINOR)     << 16   )   |\
                                (   (LIBPATH_VER_REVISION)  << 24   )   |\
                                (   (LIBPATH_VER_ALPHABETA) << 0    )   |\
                                0)

